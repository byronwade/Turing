#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn every_printable_ascii_character_has_its_own_glyph() {
        for code in 0x20u32..=0x7E {
            let character = char::from_u32(code).expect("printable ASCII");
            let bitmap = glyph(character);
            // Identity, not equality: distinct characters may share blank rows,
            // but each must resolve to its own table entry.
            let index = (code - 0x20) as usize;
            assert!(
                core::ptr::eq(bitmap, &GLYPHS[index]),
                "wrong slot for {character:?}"
            );
        }
    }

    #[test]
    fn characters_outside_printable_ascii_render_the_replacement_box() {
        for character in ['\u{0}', '\t', '\n', '\u{7F}', 'é', '中', '🦀'] {
            assert!(
                core::ptr::eq(glyph(character), &REPLACEMENT),
                "{character:?}"
            );
        }
    }

    #[test]
    fn the_space_glyph_is_blank_and_the_underscore_touches_the_bottom_row() {
        assert_eq!(glyph(' '), &[0u8; 8]);
        assert_eq!(glyph('_')[7], 0xFF);
        assert_eq!(&glyph('_')[..7], &[0u8; 7]);
    }
}
