//! The reference glyph set: an 8x8 monochrome bitmap font.
//!
//! # Provenance
//!
//! The glyph bitmaps are the printable-ASCII range of `font8x8_basic` from
//! Daniel Hepper's `font8x8` collection, itself derived from Marcel Sondaar's
//! public-domain VGA fonts. Both publications are explicitly public domain, so
//! embedding the data imposes no license obligation; the attribution here is
//! recorded because provenance is how the data stays reviewable, not because a
//! license demands it.
//!
//! # Encoding
//!
//! One glyph is eight bytes, one byte per row from top to bottom. Within a
//! byte, bit 0 is the leftmost column — the least significant bit is on the
//! left, which is the `font8x8` convention and the opposite of reading order.
//!
//! # Scope
//!
//! Exactly the ninety-five printable ASCII characters, `' '` through `'~'`.
//! Everything else renders as [`REPLACEMENT`], a hollow box that cannot be
//! mistaken for a real glyph. This set is the text-font foundation for the
//! reference rasterizer only: it decides what the CPU reference painter draws,
//! and it deliberately decides nothing about shaping, hinting, Unicode
//! coverage, or the product font stack.

/// Width and height of every glyph, in pixels.
pub(crate) const GLYPH_SIZE: usize = 8;

/// The glyph drawn for any character outside printable ASCII: a hollow box.
///
/// This is the one invented shape in the set. It exists so unsupported text is
/// visibly a placeholder — a page full of boxes reads as "missing glyphs",
/// while silently dropping characters would read as "nothing was here".
pub(crate) static REPLACEMENT: [u8; 8] = [0x7F, 0x41, 0x41, 0x41, 0x41, 0x41, 0x7F, 0x00];

/// Returns the bitmap for `character`.
pub(crate) fn glyph(character: char) -> &'static [u8; 8] {
    let code = u32::from(character);
    let first = u32::from(FIRST);
    if (first..first + GLYPH_COUNT).contains(&code) {
        let index = (code - first) as usize;
        &GLYPHS[index]
    } else {
        &REPLACEMENT
    }
}

